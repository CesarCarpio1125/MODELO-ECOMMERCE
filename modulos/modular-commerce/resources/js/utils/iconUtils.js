/**
 * Get color classes for background and text
 * @param {string} colorName - The name of the color
 * @param {string} type - 'bg' for background, 'text' for text, 'both' for both
 * @returns {string} CSS classes
 */
export function getColorClasses(colorName, type = 'both') {
    const colors = {
        blue: {
            bg: 'bg-blue-100',
            text: 'text-blue-600'
        },
        green: {
            bg: 'bg-green-100',
            text: 'text-green-600'
        },
        purple: {
            bg: 'bg-purple-100',
            text: 'text-purple-600'
        },
        red: {
            bg: 'bg-red-100',
            text: 'text-red-600'
        },
        orange: {
            bg: 'bg-orange-100',
            text: 'text-orange-600'
        },
        yellow: {
            bg: 'bg-yellow-100',
            text: 'text-yellow-600'
        },
        gray: {
            bg: 'bg-gray-100',
            text: 'text-gray-600'
        }
    };

    const color = colors[colorName] || colors.gray;

    switch (type) {
        case 'bg':
            return color.bg;
        case 'text':
            return color.text;
        case 'both':
        default:
            return `${color.bg} ${color.text}`;
    }
}

/**
 * Get icon classes combining background and text colors
 * @param {string} iconName - The name of the icon
 * @param {string} colorName - The name of the color
 * @returns {string} CSS classes for icon container
 */
export function getIconClasses(iconName, colorName) {
    return getColorClasses(colorName, 'both');
}
